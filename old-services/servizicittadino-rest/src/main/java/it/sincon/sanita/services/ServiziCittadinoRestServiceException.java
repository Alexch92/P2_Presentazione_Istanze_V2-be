package it.sincon.sanita.services;

public class ServiziCittadinoRestServiceException extends Exception {
	public ServiziCittadinoRestServiceException() {
		super();
	}

	public ServiziCittadinoRestServiceException(String msg) {
		super(msg);
	}

	public ServiziCittadinoRestServiceException(String msg, Throwable cause) {
		super(msg, cause);
	}

	public ServiziCittadinoRestServiceException(Throwable cause) {
		super(cause);
	}
}
